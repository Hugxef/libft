#include "libft.h"

int	ft_isalnum(int i)
{
	if ('0' <= i && i <= '9')
	{
		return (1);
	}
	else if ('a' <= i && i <= 'z')
	{
		return (1);
	}
	else if ('A' <= i && i <= 'Z')
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
